/**
 * add - addition
 * @a: first number
 * @b: second number
 * Return: sum of first number and second number
 */
int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtraction
 * @a: first number
 * @b: second number
 * Return: difference of first number and second number
 */
int sub(int a, int b)
{
	return a - b;
}

/**
 * mul - multiplication
 * @a: first number
 * @b: second number
 * Return: product of first number and second number
 */
int mul(int a, int b)
{
	return a * b;
}

/**
 * div - division
 * @a: first number
 * @b: second number
 * Return: divison of first number and second number
 */
int div(int a, int b)
{
	return a / b;
}

/**
 * mod - modulo
 * @a: first number
 * @b: second number
 * Return: sum of first number and second number
 */
int mod(int a, int b)
{
	return a % b;
}
